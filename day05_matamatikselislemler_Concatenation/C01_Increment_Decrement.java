package day05_matamatikselislemler_Concatenation;

public class C01_Increment_Decrement {

    public static void main(String[] args) {

        int sayi = 20;

        sayi = sayi - 8;

        System.out.println("Cevab: " + sayi);

        sayi =-2;

        System.out.println("Cevab2: " + sayi);

        sayi += 20;

        System.out.println("Cevab: " + sayi);

        sayi ++;

        System.out.println("Cevab: " + sayi);
    }
}
